package code;

/**
 *
 * @author Bcas Campus
 */
public class customerCode {
    
    String c_id;
    String name;
    String telephone;
    String email;

    public customerCode(String c_id, String name, String telephone, String email) {
        this.c_id = c_id;
        this.name = name;
        this.telephone = telephone;
        this.email = email;
    }

    public String getC_id() {
        return c_id;
    }

    public void setC_id(String c_id) {
        this.c_id = c_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
   
}
